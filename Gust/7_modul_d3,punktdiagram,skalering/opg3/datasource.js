function GetDataPoints(amount) {
    let result = [];
    for (let i=0; i < amount; i++) {
        let x = Math.floor(Math.random()*400)-200;

        let y = Math.floor((x*x/4 + 8*x+9)/17);

        y += Math.floor(Math.random()*20)-10;

        result.push([x,y]);

    }
    return result;
}

function ArrayToString(a){
    let res ="[";
    let i=1;
    for (let aa of a){
        res += "[" + aa.toString() + "],";
        if (i % 2 == 0)
           res += "\n";
        i++;
    }
    res += "]";
    return res;
}

